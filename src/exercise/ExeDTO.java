package exercise;

public class ExeDTO {
	private int exeid;
	private String exename;
	private String bmi;
	private String exepicture;
	private String exeway;
	private int exekcal;
	
	public int getExekcal() {
		return exekcal;
	}
	public void setExekcal(int exekcal) {
		this.exekcal = exekcal;
	}
	public int getExeid() {
		return exeid;
	}
	public void setExeid(int exeid) {
		this.exeid = exeid;
	}
	public String getExename() {
		return exename;
	}
	public void setExename(String exename) {
		this.exename = exename;
	}
	public String getBmi() {
		return bmi;
	}
	public void setBmi(String bmi) {
		this.bmi = bmi;
	}
	public String getExepicture() {
		return exepicture;
	}
	public void setExepicture(String exepicture) {
		this.exepicture = exepicture;
	}
	public String getExeway() {
		return exeway;
	}
	public void setExeway(String exeway) {
		this.exeway = exeway;
	}
	

}
